my_string = input('Напиши любой текст: ')

print('В твоём тексте', len(my_string) , 'символов')
print( 'Твой текст большими буквами - ' , my_string .upper())
print( 'Твой текст маленькими буквами - ' , my_string .lower())
print( 'Твой текст без пробелов - ' , my_string .replace(' ', ''))
print( 'Первая буква текста - ' , my_string [0])
print( 'Последняя буква текста - ' , my_string [-1])
